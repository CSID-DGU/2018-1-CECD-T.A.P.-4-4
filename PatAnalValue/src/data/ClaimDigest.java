package data;

public class ClaimDigest {
	private String patNum;
	private String ipcpc;
	private String skillNum;
	/**
	 * @return the patNum
	 */
	public String getPatNum() {
		return patNum;
	}
	/**
	 * @param patNum the patNum to set
	 */
	public void setPatNum(String patNum) {
		this.patNum = patNum;
	}
	/**
	 * @return the ipcpc
	 */
	public String getIpcpc() {
		return ipcpc;
	}
	/**
	 * @param ipcpc the ipcpc to set
	 */
	public void setIpcpc(String ipcpc) {
		this.ipcpc = ipcpc;
	}
	/**
	 * @return the skillNum
	 */
	public String getSkillNum() {
		return skillNum;
	}
	/**
	 * @param skillNum the skillNum to set
	 */
	public void setSkillNum(String skillNum) {
		this.skillNum = skillNum;
	}

}
